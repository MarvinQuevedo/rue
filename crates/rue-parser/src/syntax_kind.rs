use num_derive::{FromPrimitive, ToPrimitive};

#[derive(
    Debug, Default, Clone, Copy, PartialEq, Eq, PartialOrd, Ord, Hash, ToPrimitive, FromPrimitive,
)]
pub enum SyntaxKind {
    #[default]
    Eof,
    Error,
    Whitespace,
    LineComment,
    BlockComment,
    Ident,
    Int,
    OpenParen,
    CloseParen,
    OpenBrace,
    CloseBrace,
    Fun,
    If,
    Else,
    Comma,
    Colon,
    Arrow,
    Plus,
    Minus,
    Star,
    Slash,
    LessThan,
    GreaterThan,
    Root,
    FunctionItem,
    FunctionParamList,
    FunctionParam,
    Block,
    LiteralExpr,
    BinaryExpr,
    IfExpr,
    FunctionCall,
    FunctionCallArgs,
    LiteralType,
    FunctionType,
    FunctionTypeParams,
}
